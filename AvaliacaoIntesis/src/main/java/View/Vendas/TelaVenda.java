    private void tblProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdutosMouseClicked
        // TODO add your handling code here:
        int numberOfLines = tblCarrinho.getRowCount();
        int line;
        int lineSelected = tblProdutos.getSelectedRow();
        String nome = tblProdutos.getValueAt(lineSelected, 0).toString();
        String produtoExistente;
        Double preco = Double.parseDouble(tblProdutos.getValueAt(lineSelected, 1).toString());
        int quant = Integer.parseInt(txtQuantidade.getText());
        int quantExistente = 0;
        Double total = quant*preco;
        
        DefaultTableModel modelCarrinho = (DefaultTableModel) tblCarrinho.getModel();
        
        
        for(line=0;line<numberOfLines;line++){
            produtoExistente = tblProdutos.getValueAt(line, 0).toString();
            quantExistente = Integer.parseInt(tblCarrinho.getValueAt(line, 1).toString());
            if(produtoExistente.equals(nome)){
                quantExistente += quant; 
                tblCarrinho.setValueAt(quantExistente,line,1);
            }
        }
        
        int result = JOptionPane.showConfirmDialog(null, "Deseja incluir " + quant + "x " + nome + " ao carrinho?",
                "Alerta de inclusao",JOptionPane.OK_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
        
        if(result == JOptionPane.OK_OPTION){
            addProdutoAtCarrinho(modelCarrinho,nome,quant,preco,total);   
        }
        else{
            JOptionPane.showMessageDialog(null, "O produto não foi adicionado ao carrinho");
        }
        
        calculaTotal();
    }//GEN-LAST:event_tblProdutosMouseClicked

    private void addProdutoAtCarrinho(DefaultTableModel modelCarrinho, String nome, int quant, Double preco, Double total){
        modelCarrinho.addRow(new Object[]{
            nome,
            quant,
            preco,
            total 
            });
    }    
