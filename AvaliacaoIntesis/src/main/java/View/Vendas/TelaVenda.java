    private void btnExcluiProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluiProdutoActionPerformed
        // TODO add your handling code here:

        int line = tblCarrinho.getSelectedRow();
        
        if(line==(-1)){
            JOptionPane.showMessageDialog(null, "Um item tem que estar selecionado para ser exlcuido");
        }
        else
        {
        
        DefaultTableModel model = (DefaultTableModel) tblCarrinho.getModel();
        
        int choice = 0;
        Object[] opcoes = {"Sim", "Nao"};
            choice = JOptionPane.showOptionDialog(null, "Deseja remover esse produto do carrinho?", "Alerta de exclusao", 
                    JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, 
                    opcoes, opcoes[0]);
            
        switch(choice){
            case 0: 
            model.removeRow(line);
            JOptionPane.showMessageDialog(null, "O produto foi removido");
            break;
                
            case 1: 
            JOptionPane.showMessageDialog(null, "O produto nao foi removido");
            break;
        }
        }
        
        calculaTotal();
    }//GEN-LAST:event_btnExcluiProdutoActionPerformed

