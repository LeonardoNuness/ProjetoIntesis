    private void tblCarrinhoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblCarrinhoKeyReleased
        // TODO add your handling code here:
        int lineSelected = tblCarrinho.getSelectedRow();
        Double preco = Double.parseDouble(tblCarrinho.getValueAt(lineSelected, 2).toString());
        int quant = Integer.parseInt(tblCarrinho.getValueAt(lineSelected, 1).toString());
        Double total = quant*preco;
        
        tblCarrinho.setValueAt(total, lineSelected, 3);
        
        calculaTotal();
    }//GEN-LAST:event_tblCarrinhoKeyReleased

    public void calculaTotal(){
        int numberOfLine = tblCarrinho.getRowCount();
        int line;
        double value, totalResult = 0;
        
        for(line=0;line<numberOfLine;line++){
            value = Double.parseDouble(tblCarrinho.getValueAt(line, 3).toString());
            totalResult += value;
        }
        
        txtTotalaVenda.setText(String.valueOf(totalResult));
    }
    
    public void save(){
        saveVenda();
        saveProdutos();
        
    }
    
    public void saveProdutos(){
        int numberOfLines = tblCarrinho.getRowCount();
        int line;
        String nome;
        Double preco;
        int quant;
        
        Venda_ProdutoDAO vpDAO = new Venda_ProdutoDAO();
        List<Venda_Produtos> listVendaProdutos = new ArrayList<>();
        Venda_Produtos vp = new Venda_Produtos();
        
        for(line=0;line<numberOfLines;line++){
            nome = tblCarrinho.getValueAt(line,0).toString();
            preco = Double.parseDouble(String.valueOf(tblCarrinho.getValueAt(line,2)));
            quant = Integer.parseInt(String.valueOf(tblCarrinho.getValueAt(line,1)));
            
            //vp.setId();
            vp.setNome(nome);
            vp.setPreco(preco);
            vp.setQuantidade(quant);
            
            listVendaProdutos.add(vp);
        }
        
        vpDAO.createVendaProdutos(listVendaProdutos);
        
        
        
    }
    
    public void saveVenda(){
        Venda v = new Venda();
        VendaDAO vDAO = new VendaDAO();
        
        String formaPagamento = formaDePagamento();
        int idCliente = cmbCliente.getSelectedIndex()+1;
        Date d = new Date();
        
        try{
            v.setTotalVenda(Double.parseDouble(txtTotalaVenda.getText()));
            v.setDataVenda(d);
            v.setFormaPagamento(formaPagamento);
            v.setCliente(idCliente);
            
        } catch (NumberFormatException ex){}
        
        vDAO.createVenda(v);
        
    }
    
    public String formaDePagamento(){
        String formaPagamento = null;
        Object[] opcoes = { "Pix", "Debito", "Credito", "VA" };
            int formaEscolhida = JOptionPane.showOptionDialog(null, "Qual a forma de pagamento", "Forma de pagamento", 
                    JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, 
                    opcoes, opcoes[0]);
        switch(formaEscolhida){
            case 0: 
                formaPagamento = "Pix";
            break;
                
            case 1: 
                formaPagamento = "Debito";
            break;
            
            case 2: 
                formaPagamento = "Credito";
            break;
            
            case 3: 
                formaPagamento = "VA";
            break;
        }
        return formaPagamento;
    }
    
